print("Please input day of the week(mon ~ sun) : ")
dow = input()

if dow == 'mon':
    print("Monday")
elif dow == 'tue':
    print("Tuesday")
elif dow == 'wed':
    print("Wednesday")
elif dow == 'thu':
    print("Thursday")
elif dow == 'fri':
    print("Friday")
elif dow == 'sat':
    print("Saturday")
elif dow == 'sun':
    print("Sunday")
else:
    print("Invalid input")